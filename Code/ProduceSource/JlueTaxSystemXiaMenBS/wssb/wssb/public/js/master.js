
function mousein(td){
	if(td.id=="td1")
		td.bgColor="#FF7818";
	else	if(td.id=="td2")
		td.bgColor="#A8A8A8"
			if(td.id=="td3")
		td.bgColor="#000000"
//	td2.bgColor="black";
}

function mouseout(td){
	if(td.id=="td1")
		td.bgColor="#0077777";
	else	if(td.id=="td2")
		td.bgColor=""
			if(td.id=="td3")
		td.bgColor="#2798A0"
//	td2.bgColor="white";
}
